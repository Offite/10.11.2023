class Student:

  def __init__(self, name, age, grade_point_average):
    self.name = name
    self.age = age
    self.grade_point_average = grade_point_average

  def get_name(self):
      return self.name

  def set_name(self, name):
      self.name = name

  def get_age(self):
      return self.age

  def set_age(self, age):
     self.age = age

  def get_grade_point_average(self):
      return self.grade_point_average

  def set_grade_point_average(self, grade_point_average):
      self.grade_point_average = grade_point_average

      